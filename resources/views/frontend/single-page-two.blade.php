                        <p>Economist and social thinker Dr Hossain Zillur Rahman is a leading policy voice of Bangladesh with over three decades of experience within and outside government. Holding a masters in economics from Dhaka University, and PhD in political sociology from the University of Manchester, UK, Dr Rahman was a leading researcher at the Bangladesh Institute of Development Studies between 1977 and 2000, and led the internationally renowned Analysis of Poverty Trends project (1989-98). In 1996, he founded the think tank Power and Participation Research Centre (PPRC) and has been its executive chairman since 2000. He was the lead consultant for the preparation of the first poverty reduction strategy paper, Unlocking the Potential, 2005, of the Government of Bangladesh and was a member of the SAARC Poverty Commission. Dr Rahman has also sat on the board of the central bank.
                            <br>
                            <br> He authored and edited many influential publications on poverty, social protection, governance, urbanisation, inclusive growth, social development and sustainable healthcare. Some of his noted works include Rethinking Rural Poverty (SAGE, 1995); Local Governance and Community Capacities (UPL, 2002); Unbundling Governance (PPRC, 2007); Researching Poverty from the Bottom up (PPRC/Grameen Trust, 2007); Bangladesh 2030: Strategy for Accelerating Inclusive Growth (DCCI/PPRC, 2010); Bangladesh Urban Dynamics (PPRC/World Bank, 2012); Bangladesh: Primary Education Stipends (PPRC/UNICEF, 2013); Social Protection in Bangladesh (UPL, 2014); Road Safety in Bangladesh (PPRC/BRAC, 2014); Realizing UHC Goals: Challenges &amp; Opportunities for Bangladesh (PPRC, 2016); The Urban Spectrum: Metropolitan to Mofussil (PPRC/BBS, 2016); Exploring a more effective pro-poor targeting approach (PPRC/USAID, 2018).
                            <br>
                            <br> He has been a consultant to international organisations such as The World Bank, ADB, DFID, JICA, JBIC, UNDP, UNICEF, SIDA, SDC, WFP and UNFPA. As a regular resource person at the National Defense College, Dhaka, Dr Rahman lectures on nation-state system, world economic scene, national security and sustainable development. He is also the convener of civic platforms such as Health Bangladesh, and Safe Roads and Transport for All (SROTA).
                            <br>
                            <br> On 9 January 2008, Dr Rahman was appointed as an advisor to the former caretaker government of Bangladesh and put in charge of the ministries of commerce and education. He served till the end of the tenure of this government on 6 January 2009, and was credited with a lead role in the successful return of electoral democracy to Bangladesh.
                            <br>
                            <br> He was awarded Dr John Meyer Global Citizenship Award by the Institute for Global Leadership of Tufts University, USA, in November 2009. He became one of the three awardees of the Gold Medal Award 2013 of Rotary International Bangladesh for his services to humanity.</p>
