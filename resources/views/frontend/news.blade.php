                        <p>Mr Obaidul Quader, Minister of Road Transport and Bridges, said, “Women drive vehicles abiding by the rules and in a calm manner. They are usually not substance abusers. They do not talk on mobile phones while driving. So the more women will be appointed as professional drivers, the lesser will be the risks of road accidents.”</p>
                        <p>The minister’s comment came today on Saturday (25 January) as he was addressing an audience as the chief guest of a ceremony to hand out certificates to women who successfully completed a course at BRAC Driving School in Dhaka city. Ms Kazi Rowshan Akhter, Secretary of the Ministry of Women and Children’s Affairs, attended the event titled ‘Women behind the wheel for road safety’ as special guest. Mr Asif Saleh, executive director of BRAC, chaired the ceremony.</p>
                        <p>BRAC’s initiative to train women as professional drivers to promote both road safety and women-friendly commuting system has drawn praise at home and abroad. This ceremony was organised for the 8th batch of learners in which 11 women enrolled and all of them passed and received driving licences. The 3-month training was conducted at BRAC Learning Centre at Uttara in the capital.</p>
                        <p>The ceremony began with one-minute silence paying tribute to the memory of BRAC’s founder Sir Fazle Hasan Abed. The main event started with a presentation titled ‘Women behind the wheel for road safety’ by Mr Ahmed Najmul Hussain, director of BRAC Administration and Road Safety Programme. According to the presentation, BRAC started its driving school in 2011, mainly to train people from disadvantaged families to produce skilled and quality professional vehicle drivers. Its 3-month residential course curriculum includes basic driving skills, safe driving techniques, basic repairs skills and professional conduct.</p>
                        <p>Mr Obaidul Quader further said in his speech, “We want other institutions to come forward to help women gain self-dependence. The aim of these initiatives will simply go in vain if women fail to get jobs even after successfully completing such several-month long courses. So both the government and private sectors have to clear their way to recruit women drivers. The government will consider this issue with more importance from now on.”</p>
                        <p>Kazi Rowshan Akhter in her secretary’s speech said, “It is also from a BRAC’s research we have learned that 94 percent women public transport users face one or other kind of sexual harassment. Women drivers in our country also deal with many kinds of challenges. We have to join hands in finding the roots of those challenges as well as the solutions.”</p>
                        <p>Ms Mercy Miyang Tembon, country representative of World Bank for Bangladesh and Bhutan, noted writer and researcher Mr Syed Abul Maksud, BRAC’s director Ms Anna Minj and representatives from Gono Shasthya Kendra, Nirapad Sarak Chai movement and road transport owners association were also present at the event.</p>
                        <p>Organisers further said BRAC Driving School so far trained 7,388 individuals in basic driving among which 1,973 are women. It trained 10,373 in professional driving of which 214 are women. A total of 599 women received training in motorbike driving. A large number of graduates from the BRAC Driving School are successfully working for different local, international and corporate organisations.</p>
                        <p>Since 2011, under BRAC’s community road safety awareness programme, over 1.2 million people have received training on safe use of roads. Also 5,451 schoolteachers and 498,000 schoolchildren received special awareness training on safe road use.</p>
                        <p>BRAC’s executive director Mr Asif Saleh in his chair’s speech said, “BRAC is considering taking its driving training school to every division and district of the country. BRAC also wants to establish a road safety institute. BRAC will continue its collaborative work with the government in future to ensure road safety, particularly to create a women-friendly road and transport system.”</p>
