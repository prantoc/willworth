                        <p>BRAC's finance and accounts department is responsible for financial planning and record-keeping, managing costs, assets and liabilities, financing investments, analysing cash-flow and profitability, and preparing budgets and financial frameworks for the development programmes. The department is accountable for donor grants, property, provident funds, employee gratuity, salary, tax and loans. It produces annual and monthly consolidated financial statements and fulfils the rules and regulations of the NGO Affairs Bureau, Microcredit Regulatory Authority (MRA) and other regulatory bodies. Through this department we ensure effective financial control and transparency of the financial data of our projects and enterprises, garnering the trust and confidence from all those with whom we work.</p>
